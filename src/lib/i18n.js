function applyVars(template, vars) {
  if (!vars) {
    return template
  }
  return Object.entries(vars).reduce(
    (result, [key, value]) => result.replace(new RegExp(`{{\\s*${key}\\s*}}`, 'g'), String(value)),
    template,
  )
}

export function tByLang(lang, key, vars) {
  const current = messages[lang] ?? messages.en
  const value = current[key] ?? messages.en[key] ?? key
  return typeof value === 'string' ? applyVars(value, vars) : value
}

export function translateEnum(lang, group, value) {
  const translated = enumDictionary[group]?.[value]
  return translated?.[lang] ?? translated?.en ?? value
}
