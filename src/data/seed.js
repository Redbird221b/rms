function buildAuditTrail(risk) {
  return [
    {
      id: `${risk.id}-AUD-1`,
      type: 'create',
      title: 'Risk registered',
      notes: 'Initial assessment submitted by first-line owner.',
      by: risk.owner,
      at: risk.createdAt,
    },
    {
      id: `${risk.id}-AUD-2`,
      type: 'review',
      title: `Status set to ${risk.status}`,
      notes: risk.committee.lastDecision ? `Committee action: ${risk.committee.lastDecision}` : '',
      by: 'Risk Office',
      at: risk.updatedAt,
    },
  ]
}

